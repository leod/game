extern std::vector<vec3> rawPositions;
extern std::vector<vec3> rawNormals;
extern std::vector<GLshort> rawIndices;

RenderCube::RenderCube(PhysicsComponent const* physics, vec3 color)
    : physics(physics), color(color),
      positions(GL_ARRAY_BUFFER, rawPositions),
      normals(GL_ARRAY_BUFFER, rawNormals),
      indices(GL_ELEMENT_ARRAY_BUFFER, rawIndices) {
}

void RenderCube::render() {
    auto system = getEntities()->system<RenderSystem>();
    auto program = system->getPrograms().load("shaders/cube_vertex.glsl",
                                              "shaders/cube_fragment.glsl");
    auto model = mat4();
    {
        auto position = physics->getPosition();
        auto orientation = physics->getOrientation();
        auto angle = glm::atan(orientation.x, orientation.z);

        model = glm::translate(model, position);
        model = glm::rotate(model, glm::degrees(angle) - 90, vec3(0, 1, 0));
        model = glm::scale(model, vec3(3, 3, 3));
    }

    program->bind();
    program->setUniform(program->getUniformLocation("projection"),
                        system->getProjection());
    program->setUniform(program->getUniformLocation("view"),
                        system->getView());
    program->setUniform(program->getUniformLocation("model"),
                        model);
    program->setUniform(program->getUniformLocation("diffuse"),
                        color);

    system->getVisionTexture().bind();
    program->setUniform(program->getUniformLocation("vision"), 0);

    program->setAttrib(0, positions);
    program->setAttrib(1, normals);

    indices.bind();

    {
        size_t i = 0, start = 0;

        // Horribly inefficient but I don't care
        
        while (i < rawIndices.size()) {
            if (rawIndices[i] == -1) {
                glDrawElements(GL_TRIANGLE_STRIP,
                    i - start, GL_UNSIGNED_SHORT,
                    reinterpret_cast<GLvoid*>(start * sizeof(GLushort)));
                start = i + 1;
            }

            ++i;
        }

        ASSERT(start == rawIndices.size());
    }

    program->unsetAttrib(0);
    program->unsetAttrib(1);
    program->unbind();
    glBindTexture(GL_TEXTURE_2D, 0);
}

